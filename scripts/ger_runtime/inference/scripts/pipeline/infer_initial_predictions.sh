set -euo pipefail

export NCCL_P2P_DISABLE="1"
export NCCL_IB_DISABLE="1"

PROJECT_ROOT_GUESS="${GER_PROJECT_ROOT:-$(cd "$(dirname "${BASH_SOURCE[0]}")/../../../.." && pwd)}"
PYTHON_BIN="${GER_PYTHON:-${PROJECT_ROOT_GUESS}/.venv/bin/python}"
if [ ! -x "$PYTHON_BIN" ]; then
    PYTHON_BIN="${PYTHON:-python}"
fi

require_nonempty_file() {
    local file="$1"
    if [ ! -s "$file" ]; then
        echo "Required output file is missing or empty: $file" >&2
        exit 1
    fi
}

# Check if the first argument is provided and not empty
if [ "$#" -eq 1 ] && [ -n "$1" ]; then
    # Get YAML file path from the first argument
    YAML_FILE="$1"

    # Set environment variables by sourcing the commands generated by the Python script
    source <("$PYTHON_BIN" yaml_load.py "$YAML_FILE")
else
    echo "<<Warning>>: YAML file path is not provided or is empty. Please make sure that you have passed the required enviromental variables."
    exit 1
fi

PIPELINE_SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${PIPELINE_SCRIPT_DIR}/icl_runtime_defaults.sh"
apply_icl_runtime_defaults

INITIAL_RESULT_DIR="${GER_INITIAL_RESULT_DIR:-${GER_RESULT_DIR_ICL_RANDOM:-${RESULT_DIR_ICL_RANDOM:-}}}"
INITIAL_RESULT_SPLIT="${GER_INITIAL_RESULT_SPLIT:-${GER_RANDOM_RESULT_SPLIT:-test}}"
INITIAL_RESULT_SUFFIX="${GER_INITIAL_RESULT_SUFFIX:-${GER_RANDOM_RESULT_SUFFIX:-_8}}"
if [ -z "$INITIAL_RESULT_DIR" ]; then
    INITIAL_RESULT_MODE="${INITIAL_RESULT_MODE:-default}"
    if [ -z "${MODEL_KEY:-}" ]; then
        echo "MODEL_KEY is required to infer INITIAL_RESULT_DIR." >&2
        exit 1
    fi
    INITIAL_RESULT_DIR="results_${MODEL_KEY}_${INITIAL_RESULT_MODE}/initial_predictions_${INITIAL_RESULT_SPLIT}${INITIAL_RESULT_SUFFIX}"
fi

INFERENCE_BATCH_SIZE="${GER_INFERENCE_BATCH_SIZE:-4}"
MAX_NEW_TOKENS="${GER_MAX_NEW_TOKENS:-512}"
SEED="${GER_SEED:-${SEED:-88}}"
DO_SAMPLE="${GER_DO_SAMPLE:-${DO_SAMPLE:-0}}"
TEMPERATURE="${GER_TEMPERATURE:-${TEMPERATURE:-0.0}}"
TOP_P="${GER_TOP_P:-${TOP_P:-1.0}}"
NUM_SHARDS="${GER_NUM_SHARDS:-1}"
SHARD_ID="${GER_SHARD_ID:-}"
SHARD_GPU_LIST="${GER_SHARD_CUDA_VISIBLE_DEVICES:-$CUDA_VISIBLE_DEVICES}"
DIALOGUE_ARGS=()
case "${DIALOGUE_FORM}" in
    1|true|True|TRUE|yes|Yes|YES)
        DIALOGUE_ARGS+=(--dialogue_form)
        ;;
esac
SAMPLE_ARGS=()
case "${DO_SAMPLE}" in
    1|true|True|TRUE|yes|Yes|YES)
        SAMPLE_ARGS+=(--do_sample)
        ;;
esac
INFER_MODE_ARGS=()
case "${INITIAL_RESULT_SPLIT}" in
    train)
        INFER_MODE_ARGS+=(--infer_mode=train)
        ;;
    valid|validation|eval)
        INFER_MODE_ARGS+=(--infer_mode=valid)
        ;;
    test)
        INFER_MODE_ARGS+=(--infer_mode=test)
        ;;
    *)
        echo "Unsupported GER_INITIAL_RESULT_SPLIT=${INITIAL_RESULT_SPLIT}. Use train, valid, or test." >&2
        exit 1
        ;;
esac

# conversion
RESULT_DIR=$INITIAL_RESULT_DIR
IFS=':' read -r first_part second_part <<< "$DATASET"
INFERENCE_DATASET_NAME=${first_part}

# delete old results
# rm -r $RESULT_DIR

if [ "$NUM_SHARDS" -gt 1 ] && [ -z "$SHARD_ID" ]; then
    IFS=',' read -r -a SHARD_GPUS <<< "$SHARD_GPU_LIST"
    if [ "${#SHARD_GPUS[@]}" -eq 0 ]; then
        echo "No GPUs found in SHARD_GPU_LIST=$SHARD_GPU_LIST"
        exit 1
    fi

    PROGRESS_STOP_FILE="${RESULT_DIR}/${INFERENCE_DATASET_NAME}/.shards_progress.done"
    mkdir -p "$(dirname "$PROGRESS_STOP_FILE")"
    rm -f "$PROGRESS_STOP_FILE"

    pids=()
    monitor_pid=""
    use_setsid=0
    if command -v setsid >/dev/null 2>&1; then
        use_setsid=1
    fi

    cleanup_shards() {
        local exit_code="${1:-130}"
        trap - INT TERM
        echo "Stopping initial-prediction shard workers..." >&2
        touch "$PROGRESS_STOP_FILE" 2>/dev/null || true
        if [ -n "$monitor_pid" ]; then
            kill -TERM "$monitor_pid" 2>/dev/null || true
        fi
        for pid in "${pids[@]}"; do
            if [ "$use_setsid" -eq 1 ]; then
                kill -TERM "-$pid" 2>/dev/null || true
            else
                kill -TERM "$pid" 2>/dev/null || true
            fi
        done
        sleep 2
        for pid in "${pids[@]}"; do
            if [ "$use_setsid" -eq 1 ]; then
                kill -KILL "-$pid" 2>/dev/null || true
            else
                kill -KILL "$pid" 2>/dev/null || true
            fi
        done
        exit "$exit_code"
    }
    trap 'cleanup_shards 130' INT
    trap 'cleanup_shards 143' TERM

    for shard in $(seq 0 $((NUM_SHARDS - 1))); do
        gpu="${SHARD_GPUS[$((shard % ${#SHARD_GPUS[@]}))]}"
        echo "Launching initial-prediction shard ${shard}/${NUM_SHARDS} on GPU ${gpu}"
        if [ "$use_setsid" -eq 1 ]; then
            GER_DISABLE_CHILD_PROGRESS=1 GER_QUIET_CHILD_LOG=1 GER_SHARD_ID="$shard" GER_NUM_SHARDS="$NUM_SHARDS" GER_CUDA_VISIBLE_DEVICES="$gpu" setsid bash "$0" "$YAML_FILE" &
        else
            GER_DISABLE_CHILD_PROGRESS=1 GER_QUIET_CHILD_LOG=1 GER_SHARD_ID="$shard" GER_NUM_SHARDS="$NUM_SHARDS" GER_CUDA_VISIBLE_DEVICES="$gpu" bash "$0" "$YAML_FILE" &
        fi
        pids+=("$!")
    done
    echo "All ${NUM_SHARDS} initial-prediction shards launched in parallel; waiting for all shard workers."

    "$PYTHON_BIN" scripts/monitor_sharded_progress.py \
        --dir="${RESULT_DIR}/${INFERENCE_DATASET_NAME}" \
        --num-shards="$NUM_SHARDS" \
        --desc="${INFERENCE_DATASET_NAME} shards" \
        --stop-file="$PROGRESS_STOP_FILE" &
    monitor_pid="$!"

    status=0
    for pid in "${pids[@]}"; do
        wait "$pid" || status=1
    done
    touch "$PROGRESS_STOP_FILE"
    wait "$monitor_pid" || true
    trap - INT TERM
    if [ "$status" -ne 0 ]; then
        exit "$status"
    fi

    "$PYTHON_BIN" scripts/merge_sharded_predictions.py \
        --dir="${RESULT_DIR}/${INFERENCE_DATASET_NAME}" \
        --num_shards="$NUM_SHARDS"
else
    SHARD_ID="${SHARD_ID:-0}"
    echo "Running initial-prediction shard ${SHARD_ID}/${NUM_SHARDS} with CUDA_VISIBLE_DEVICES=${CUDA_VISIBLE_DEVICES} (pid $$)"
    CUDA_VISIBLE_DEVICES="$CUDA_VISIBLE_DEVICES" "$PYTHON_BIN" icl.py \
        --datasets="$DATASET" \
        --prompts="$PROMPT_ICL" \
        --trust_remote_code=true \
        --output_dir=$RESULT_DIR \
        --max_new_tokens="$MAX_NEW_TOKENS" \
        --postprocess="gec" \
        --model_name_or_path="$MODEL" \
        --in_domain_example_num=$EXAMPLE_NUM_ERROR \
        --cross_domain_example_num=$EXAMPLE_NUM_CORRECT \
        --in_domain_filter="error" \
        --cross_domain_filter="correct" \
        --in_domain_example_mode="default" \
        --cross_domain_example_mode="default" \
        --inference_batch_size="$INFERENCE_BATCH_SIZE" \
        --num_shards="$NUM_SHARDS" \
        --shard_id="$SHARD_ID" \
        --seed="$SEED" \
        --temperature="$TEMPERATURE" \
        --top_p="$TOP_P" \
        "${SAMPLE_ARGS[@]}" \
        "${INFER_MODE_ARGS[@]}" \
        "${DIALOGUE_ARGS[@]}"

    if [ "$NUM_SHARDS" -gt 1 ]; then
        exit 0
    fi
fi

# add --bf16 if use bfloat16


# evaluate
"$PYTHON_BIN" evaluate_gec.py \
    --dir="${RESULT_DIR}/${INFERENCE_DATASET_NAME}" \
    --dataset="${INFERENCE_DATASET_NAME}" \
    --device="0"

require_nonempty_file "${RESULT_DIR}/${INFERENCE_DATASET_NAME}/predictions.jsonl"
if [ "$INFERENCE_DATASET_NAME" = "bea19" ]; then
    require_nonempty_file "${RESULT_DIR}/${INFERENCE_DATASET_NAME}/bea19.txt"
    require_nonempty_file "${RESULT_DIR}/${INFERENCE_DATASET_NAME}/bea19.zip"
else
    require_nonempty_file "${RESULT_DIR}/${INFERENCE_DATASET_NAME}/${INFERENCE_DATASET_NAME}-output-retokenized.txt"
fi

PREPROCESS_DATA="${GER_PREPROCESS_DATA:-${PROJECT_ROOT_FROM_PIPELINE}/utils/preprocess_data.py}"
if [ "$INITIAL_RESULT_SPLIT" = "train" ]; then
    if [ -n "${GER_TRAIN_SOURCE_FILE:-}" ]; then
        INITIAL_SOURCE="${GER_TRAIN_SOURCE_FILE}"
    else
        TRAIN_DATA_DIR="${GER_TRAIN_DATA_DIR:-${PROJECT_ROOT_FROM_PIPELINE}/multilingual/runtime_train_data}"
        INITIAL_SOURCE="${TRAIN_DATA_DIR}/${INFERENCE_DATASET_NAME}/train.src"
    fi
else
    INITIAL_SOURCE="${GER_TEST_SOURCE_FILE:-${PROJECT_ROOT_FROM_PIPELINE}/datasets/multilingual/${INFERENCE_DATASET_NAME}/test.src}"
fi
if [ "$INFERENCE_DATASET_NAME" = "bea19" ]; then
    INITIAL_TARGET="${RESULT_DIR}/${INFERENCE_DATASET_NAME}/bea19.txt"
else
    INITIAL_TARGET="${RESULT_DIR}/${INFERENCE_DATASET_NAME}/${INFERENCE_DATASET_NAME}-output-retokenized.txt"
fi
INITIAL_LABEL="${RESULT_DIR}/${INFERENCE_DATASET_NAME}/${INFERENCE_DATASET_NAME}-output-retokenized.label"
if [ "$INFERENCE_DATASET_NAME" = "bea19" ]; then
    cp "${INITIAL_TARGET}" "${RESULT_DIR}/${INFERENCE_DATASET_NAME}/${INFERENCE_DATASET_NAME}-output-retokenized.txt"
fi
"$PYTHON_BIN" "${PREPROCESS_DATA}" -s "${INITIAL_SOURCE}" -t "${INITIAL_TARGET}" -o "${INITIAL_LABEL}"
require_nonempty_file "${INITIAL_LABEL}"
